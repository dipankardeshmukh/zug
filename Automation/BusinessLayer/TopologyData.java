package BusinessLayer;

public class TopologyData {
	 int topologyId;
	 int role;
	 int machinecatalog;
	 String rationale;
	 String osArchitecture;
	 String osVersion;
	 String osLanguageId;
	 String osServicePack;
	 String baseVmIdentifier;
	public int getTopologyId() {
		return topologyId;
	}
	public void setTopologyId(int topologyId) {
		this.topologyId = topologyId;
	}
	
	public int getRole() {
		return role;
	}
	public void setRole(int role) {
		this.role = role;
	}
	public int getMachinecatalog() {
		return machinecatalog;
	}
	public void setMachinecatalog(int machinecatalog) {
		this.machinecatalog = machinecatalog;
	}
	public String getRationale() {
		return rationale;
	}
	public void setRationale(String rationale) {
		this.rationale = rationale;
	}
	public String getOsArchitecture() {
		return osArchitecture;
	}
	public void setOsArchitecture(String osArchitecture) {
		this.osArchitecture = osArchitecture;
	}
	public String getOsVersion() {
		return osVersion;
	}
	public void setOsVersion(String osVersion) {
		this.osVersion = osVersion;
	}
	public String getOsLanguageId() {
		return osLanguageId;
	}
	public void setOsLanguageId(String osLanguageId) {
		this.osLanguageId = osLanguageId;
	}
	public String getOsServicePack() {
		return osServicePack;
	}
	public void setOsServicePack(String osServicePack) {
		this.osServicePack = osServicePack;
	}
	public String getBaseVmIdentifier() {
		return baseVmIdentifier;
	}
	public void setBaseVmIdentifier(String baseVmIdentifier) {
		this.baseVmIdentifier = baseVmIdentifier;
	}
	 
}
